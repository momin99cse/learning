package com.practice.crud;

public class Book {
	
	int id;
	String author;
	String name;
	String publisher;
	int quantity;
	
	public Book(int id, String author, String name, String publisher,
			int quantity) {
		super();
		this.id = id;
		this.author = author;
		this.name = name;
		this.publisher = publisher;
		this.quantity = quantity;
	}
	
	
	 
	
	
	
	

}
